def get_random_logo():
    """Return a random logo art string."""
    return random.choice(LOGOS)["art"].strip("\n")


def get_logo(name):
    """Return a specific logo by name, or fall back to random."""
    for logo in LOGOS:
        if logo["name"] == name:
            return logo["art"].strip("\n")
    return get_random_logo()


def list_logos():
    """Return list of available logo names."""
    return [logo["name"] for logo in LOGOS]
